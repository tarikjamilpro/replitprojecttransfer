function convertText(text: string, charMap: Record<string, string>, reverse: boolean = false): string {
  let result = '';
  const chars = reverse ? text.split('').reverse() : text.split('');
  
  for (const char of chars) {
    result += charMap[char] ?? char;
  }
  
  return result;
}

